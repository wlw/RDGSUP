package com.bricksimpe.rdg.templateId;

public class TemplateNotes {
    private String IdentifiedText;
    private int    TemplateParseUid;
    
	public void setIdentifiedText(String identifiedText) {
		IdentifiedText = identifiedText;
	}
	public String getIdentifiedText() {
		return IdentifiedText;
	}
	public void setTemplateParseUid(int templateParseUid) {
		TemplateParseUid = templateParseUid;
	}
	public int getTemplateParseUid() {
		return TemplateParseUid;
	}
}
